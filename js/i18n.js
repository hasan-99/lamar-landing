function setLanguage(lang) {
  const dir = lang === 'ar' ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  document.documentElement.dir = dir;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (!translations[lang]?.[key]) return;
    if (key === 'hero.title') {
      el.setAttribute('data-typed-text', translations[lang][key]);
      return;
    }
    if (el.tagName === 'INPUT' && el.type !== 'submit') {
      el.placeholder = translations[lang][key];
    } else if (el.tagName === 'TEXTAREA') {
      el.placeholder = translations[lang][key];
    } else {
      el.textContent = translations[lang][key];
    }
  });

  const toggle = document.getElementById('langToggle');
  if (toggle) toggle.textContent = lang === 'ar' ? 'EN' : 'عربي';
  localStorage.setItem('lamar-lang', lang);

  if (window.heroAnimationDone && typeof window.typeHeroTitle === 'function') {
    window.typeHeroTitle();
  }
}

function getCurrentLang() {
  return localStorage.getItem('lamar-lang') || 'ar';
}
